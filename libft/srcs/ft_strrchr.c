#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;

	i = ft_strlen(s) + 1;
	while (*s)
		s++;
	while (i > 0)
	{
		if (*s == c)
			return ((char*)s);
		i--;
		s--;
	}
	(void)c;
	return (NULL);
}
