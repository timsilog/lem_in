#include "libft.h"

int		ft_pow(int nb, int power)
{
	int num;

	num = 1;
	if (power < 0)
		return (0);
	if (power == 0)
		return (1);
	while (power > 0)
	{
		num *= nb;
		power--;
	}
	return (num);
}
